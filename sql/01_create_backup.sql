1. CREAR EL ESQUEMA E INSERTAR LA BASE DE DATOS.
  
En esta parte, cree el esquema de forma manual, con la herramienta que nos brinda my sql.
Partimos de la barra de herramientas superior, y hacemos click en el icono bajo la descripcion " create a new schema in the connected server". Pones el nombre que desees y le das a "apply".
  
  INSERTAR LA BASE DE DATOS
Una vez que tenemos el esquema creado, le damos click derecho sobre el mismo, y seleccionamos la opcion " Table data import wizard". Seleccionamos el archivo que contiene la BBDD y damos a seguir hasta que nos diga que se procesó correctamente.
  

